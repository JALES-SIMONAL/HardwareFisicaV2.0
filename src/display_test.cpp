void setup() {
  Serial.begin(SERIAL_BAUD_RATE);
  delay(1000);

  Serial.println("Display self-test: iniciando");
  Serial.printf("Pins: CS=%u DC=%u RST=%u SCLK=%u MOSI=%u MISO=%u BL=%u\n",
                TFT_CS, TFT_DC, TFT_RST, TFT_SCLK, TFT_MOSI, TFT_MISO, TFT_BL);
  Serial.printf("Heap livre: %u bytes\n", ESP.getFreeHeap());

  pinMode(TFT_BL, OUTPUT);
  digitalWrite(TFT_BL, HIGH);

  Serial.println("Chamada display->begin()");
  if (!display->begin()) {
    Serial.println("Falha ao iniciar o display");
    while (true) {
      delay(1000);
    }
  }
  Serial.println("display->begin() OK");

  display->fillScreen(RGB565_BLACK);
  display->setCursor(10, 10);
  display->setTextSize(2);
  display->setTextColor(RGB565_WHITE);
  display->println("Teste TFT");
  display->println("RGB565");

  display->draw16bitRGBBitmap(56, 72, displayTestBitmap, 16, 16);
  Serial.println("Imagem RGB565 desenhada");
}

